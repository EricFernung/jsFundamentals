
//VALUES = definitiions etc.
//Number => Literally a  number

let number = 10;
console.log(number);

//String = > strings are datatypes used to represent text
// wrap them in single or double quotes

let stringOne = "double quotes";
let stringTwo = "single quotes";

console.log(stringOne, stringTwo);

//Booleans => true or false

let on = true;
let off = false;
console.log(on,off);

//Null => empty value, like a container with nothing in it. Intentional

let empty = null; 
console.log(empty);

//Undefined => Does not act as an empty container, and no value is assigned, initialized, or declared.  Likely unintentional. 

let Undef = undefined;
console.log(Undef);

let correct; 
console.log(correct)

//OBJECTS
// are used to store many values.
// denoted by curly brackets {}

let frodo = {
    race: 'hobbit',
    rings: 1,
    cloak: true 
}
console.log(frodo);
console.log(typeof frodo);

//ARRAYS
// containers that hold a list of items
// denoted by square brackets []

let list = ['item1', 'item2', 'item 3']
console.log(list);

let burritos = ['large', 4, true];
console.log(burritos);
console.log(typeof burritos);

