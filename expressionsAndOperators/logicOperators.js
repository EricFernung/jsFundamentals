//ADDITION OPERATOR +=

r += 1; // r = r + 1; 

//SUBTRACTION OPERATOR -=

r -= 1; // r = r - 1; 

//MULTIPLICATION  OPERATOR *=

r *= 1; // r = r * 1; 

//DIVISION OPERATOR /=
r /= 1 // r = r / 1

//REMAINDER OR MODULUS OPERATOR %=
r %= 1; // r = r % 1

//EXPONENTIAL OPERATOR **=
r **= 2; // r = r ** 2