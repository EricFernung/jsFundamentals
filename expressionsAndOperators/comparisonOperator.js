// COMPARISON OPERATORS ==

//EQUAL TO

'3' == 3; 

console.log('3' == 3);

//STRICT EQUAL TO ===
//Compares the datatype and value of the two values to check if they are equal

'3' === 3;

console.log(3 === 3);
//same value and same datatype
console.log('3' === 3);
//same value but it is NOT the same datatype. 

//NOT EQUAL TO !=

    //false because they are equal in value
console.log('3' != 3);

//STRICT NOT EQUAL TO !==

    //true because they though they are equal in value, they are not equal in datatype
console.log('3' !== 3);

//GREATER THAN >

console.log(3 > 23);

//LESS THAN <

console.log(2 < 3);

//GREATER THAN OR EQUAL TO >=
console.log(3 >= 4);

//LESS THAN OR EQUAL TO <=
console.log(3 <= 4);

//'AND' OPERATOR &&

2 && 3;

//'OR' OPERATOR ||

2 || 3;



